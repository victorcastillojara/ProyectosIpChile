    private void mostrarLibros(){
                try{
            Class.forName("com.mysql.jdbc.Driver");
            conexion = DriverManager.getConnection(url, 
                    usuario, contrasenia);
            String sql = "SELECT * FROM libro";
            sentencia = conexion.createStatement();
            resultados = sentencia.executeQuery(sql);
            int fila = 0;
            while(resultados.next()){
                jTable3.setValueAt(resultados.getInt("id_libro"),fila, 0);
                jTable3.setValueAt(resultados.getString("titulo"),fila, 1);
                jTable3.setValueAt(resultados.getString("editorial"),fila, 2);
                jTable3.setValueAt(resultados.getString("seccion"),fila, 3);
                jTable3.setValueAt(resultados.getInt("cantidad"),fila, 4);
               // jTable3.setValueAt(resultados.getString(""),fila, 5);
                
                fila++;
            }
            sentencia.close();
            conexion.close();
            }catch(ClassNotFoundException | SQLException e){
                JOptionPane.showMessageDialog(null, 
                    "Error:"+e.getMessage(), "Error!!", 
                    JOptionPane.ERROR_MESSAGE, null);
            }
    }
    
    
